#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*d;

	d = s1;
	while (*d)
		d++;
	while (*s2 && n--)
		*d++ = *s2++;
	*d = '\0';
	return (s1);
}
